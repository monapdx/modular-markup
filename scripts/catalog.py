# Packaging
# ---------------------------------------------------------------------------


@dataclass
class PackageInfo:
    name: str
    public: bool
    zip_path: Path
    templates: int
    folders: int
    bytes: int


def package(root: Folder, meta: Metadata, out_dir: Path, version: str | None) -> list[PackageInfo]:
    out_dir.mkdir(parents=True, exist_ok=True)
    infos: list[PackageInfo] = []
    stats = {s.name: s for s in collect_stats(root)}

    for pack in root.subfolders:
        zip_path = out_dir / f"{pack.name}.zip"
        size = _zip_folder(pack.path, zip_path)
        stat = stats[pack.name]
        infos.append(
            PackageInfo(
                name=pack.name,
                public=meta.is_public(pack.name),
                zip_path=zip_path,
                templates=stat.templates,
                folders=stat.folders,
                bytes=size,
            )
        )

    manifest = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "version": version,
        "packs": [
            {
                "name": info.name,
                "public": info.public,
                "zip": info.zip_path.name,
                "templates": info.templates,
                "folders": info.folders,
                "bytes": info.bytes,
            }
            for info in infos
        ],
    }
    (out_dir / "manifest.json").write_text(
        json.dumps(manifest, indent=2) + "\n", encoding="utf-8", newline="\n"
    )
    return infos


def _zip_folder(folder: Path, zip_path: Path) -> int:
    files = sorted(
        (
            p
            for p in folder.rglob("*")
            if p.is_file() and p.suffix.lower() != ".zip"
        ),
        key=lambda p: str(p).lower(),
    )
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as archive:
        for file in files:
            archive.write(file, arcname=str(file.relative_to(folder.parent)))
    return zip_path.stat().st_size


