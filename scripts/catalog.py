def _load_root(meta: Metadata) -> Folder:
    if not LIBRARY_ROOT.exists():
        sys.exit(f"Library root not found: {LIBRARY_ROOT}")
    return scan(LIBRARY_ROOT, meta)


def cmd_generate(args: argparse.Namespace) -> int:
    meta = Metadata.load()
    root = _load_root(meta)
    result = generate(root, meta)

    print(format_stats(root, result))
    for warning in result.warnings:
        print(f"warning: {warning}", file=sys.stderr)

    if args.no_validate:
        return 0

    # Re-scan so validation sees freshly written files.
    root = _load_root(meta)
    errors = Validator(root, meta).run()
    if errors:
        _report(errors)
        return 1
    print("Validation passed")
    return 0


def cmd_validate(args: argparse.Namespace) -> int:
    meta = Metadata.load()
    root = _load_root(meta)
    errors = Validator(root, meta).run()
    if errors:
        _report(errors)
        return 1
    print("Validation passed")
    return 0


def cmd_package(args: argparse.Namespace) -> int:
    meta = Metadata.load()
    root = _load_root(meta)
    infos = package(root, meta, Path(args.out), args.version)
    for info in infos:
        tag = "public" if info.public else "private (artifact only)"
        print(f"Packaged {info.name} -> {info.zip_path.name} ({info.bytes} bytes) [{tag}]")
    print(f"Manifest written to {Path(args.out) / 'manifest.json'}")
    return 0


def cmd_stats(args: argparse.Namespace) -> int:
    meta = Metadata.load()
    root = _load_root(meta)
    print(format_stats(root))
    return 0


def _report(errors: Iterable[str]) -> None:
    errors = list(errors)
    print(f"Validation FAILED with {len(errors)} error(s):", file=sys.stderr)
    for err in errors:
        print(f"  - {err}", file=sys.stderr)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="catalog.py",
        description="Generate and validate the Modular Markup template catalog.",
    )
    sub = parser.add_subparsers(dest="command", required=True)

    gen = sub.add_parser("generate", help="Regenerate indexes, CSS and README.")
    gen.add_argument(
        "--no-validate",
        action="store_true",
        help="Skip the validation pass after generating.",
    )
    gen.set_defaults(func=cmd_generate)

    val = sub.add_parser("validate", help="Validate the catalog (non-zero on failure).")
    val.set_defaults(func=cmd_validate)

    pkg = sub.add_parser("package", help="Build per-pack ZIPs and a manifest.")
    pkg.add_argument("--out", default=str(REPO_ROOT / "dist"), help="Output directory.")
    pkg.add_argument("--version", default=None, help="Version string for the manifest.")
    pkg.set_defaults(func=cmd_package)

    st = sub.add_parser("stats", help="Print catalog statistics.")
    st.set_defaults(func=cmd_stats)

    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    return args.func(args)


if __name__ == "__main__":
    raise SystemExit(main())
